    console.log('workinh')
    deck = createDeck()
    shuffle(deck)
    for(let i = 0; i < 2; i++){
        playersCards.push(drawCard())
        dealersCards.pysh(drawCard())
    }
    playerValue = checkValue(playersCards)
    dealerValue = checkValue(dealersCards)
    playing = true
